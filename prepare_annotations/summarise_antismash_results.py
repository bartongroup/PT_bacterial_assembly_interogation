#!/usr/bin/env python3
"""
Summarise parsed antiSMASH results into isolate- and product-level TSV outputs.

This script reads the parsed antiSMASH outputs generated by
``parse_antismash_results.py`` and creates compact summary tables for quick
inspection and downstream reporting.

Inputs
------
- antismash_region_long.tsv
- antismash_product_count_matrix.tsv
- antismash_product_presence_absence_matrix.tsv

Outputs
-------
- antismash_isolate_summary_extended.tsv
- antismash_product_summary.tsv
- antismash_top_products_per_isolate.tsv
"""

from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(
        description="Summarise parsed antiSMASH result tables."
    )
    parser.add_argument(
        "--parsed_antismash_dir",
        required=True,
        help="Directory containing parsed antiSMASH TSV outputs.",
    )
    parser.add_argument(
        "--out_dir",
        required=True,
        help="Output directory for antiSMASH summary TSV files.",
    )
    parser.add_argument(
        "--top_n",
        type=int,
        default=10,
        help="Number of top product types to report per isolate.",
    )
    return parser.parse_args()


def load_required_tables(parsed_antismash_dir: Path) -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """
    Load required parsed antiSMASH tables.

    Parameters
    ----------
    parsed_antismash_dir : Path
        Directory containing parsed antiSMASH TSV files.

    Returns
    -------
    tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]
        Region table, count matrix, and presence/absence matrix.
    """
    region_tsv = parsed_antismash_dir / "antismash_region_long.tsv"
    count_tsv = parsed_antismash_dir / "antismash_product_count_matrix.tsv"
    presence_tsv = parsed_antismash_dir / "antismash_product_presence_absence_matrix.tsv"

    for required_path in [region_tsv, count_tsv, presence_tsv]:
        if not required_path.exists():
            raise FileNotFoundError(
                f"Required antiSMASH parsed file not found: {required_path}"
            )

    region_df = pd.read_csv(region_tsv, sep="\t", dtype=str)
    count_df = pd.read_csv(count_tsv, sep="\t", dtype=str)
    presence_df = pd.read_csv(presence_tsv, sep="\t", dtype=str)

    return region_df, count_df, presence_df


def summarise_isolates(region_df: pd.DataFrame, presence_df: pd.DataFrame) -> pd.DataFrame:
    """
    Build isolate-level antiSMASH summaries.

    Parameters
    ----------
    region_df : pd.DataFrame
        Region-level antiSMASH table.
    presence_df : pd.DataFrame
        Product presence/absence matrix.

    Returns
    -------
    pd.DataFrame
        Isolate-level summary table.
    """
    presence_num = presence_df.copy()
    product_columns = [column for column in presence_num.columns if column != "isolate_id"]
    presence_num[product_columns] = presence_num[product_columns].apply(pd.to_numeric)

    base_summary = (
        region_df.groupby("isolate_id", as_index=False)
        .agg(
            n_regions=("region_file", "count"),
            n_unique_products=("product", "nunique"),
            n_categories=("category", "nunique"),
            n_contig_edge_regions=("contig_edge", lambda x: (x.astype(str) == "True").sum()),
        )
    )

    base_summary["n_product_types_present"] = (
        presence_num.set_index("isolate_id")[product_columns].sum(axis=1).reindex(base_summary["isolate_id"]).values
    )

    return base_summary.sort_values(
        by=["n_regions", "n_unique_products", "isolate_id"],
        ascending=[False, False, True],
    )


def summarise_products(count_df: pd.DataFrame, presence_df: pd.DataFrame) -> pd.DataFrame:
    """
    Build product-level antiSMASH summaries.

    Parameters
    ----------
    count_df : pd.DataFrame
        Product count matrix.
    presence_df : pd.DataFrame
        Product presence/absence matrix.

    Returns
    -------
    pd.DataFrame
        Product summary table.
    """
    count_num = count_df.copy()
    presence_num = presence_df.copy()

    product_columns = [column for column in count_num.columns if column != "isolate_id"]
    count_num[product_columns] = count_num[product_columns].apply(pd.to_numeric)
    presence_num[product_columns] = presence_num[product_columns].apply(pd.to_numeric)

    records = []

    for product in product_columns:
        records.append(
            {
                "product": product,
                "n_isolates_present": int(presence_num[product].sum()),
                "n_total_regions": int(count_num[product].sum()),
                "mean_regions_per_isolate": float(count_num[product].mean()),
            }
        )

    return pd.DataFrame.from_records(records).sort_values(
        by=["n_isolates_present", "n_total_regions", "product"],
        ascending=[False, False, True],
    )


def top_products_per_isolate(count_df: pd.DataFrame, top_n: int) -> pd.DataFrame:
    """
    Build a per-isolate table of top antiSMASH product types.

    Parameters
    ----------
    count_df : pd.DataFrame
        Product count matrix.
    top_n : int
        Number of top products to report per isolate.

    Returns
    -------
    pd.DataFrame
        Long-format per-isolate product summary.
    """
    count_num = count_df.copy()
    product_columns = [column for column in count_num.columns if column != "isolate_id"]
    count_num[product_columns] = count_num[product_columns].apply(pd.to_numeric)

    records = []

    for _, row in count_num.iterrows():
        isolate_id = row["isolate_id"]
        isolate_series = row[product_columns]
        isolate_series = isolate_series[isolate_series > 0].sort_values(ascending=False).head(top_n)

        for rank, (product, value) in enumerate(isolate_series.items(), start=1):
            records.append(
                {
                    "isolate_id": isolate_id,
                    "rank": rank,
                    "product": product,
                    "region_count": int(value),
                }
            )

    if not records:
        return pd.DataFrame(
            columns=["isolate_id", "rank", "product", "region_count"]
        )

    return pd.DataFrame.from_records(records)


def main() -> None:
    """Run antiSMASH summary generation."""
    args = parse_args()
    parsed_antismash_dir = Path(args.parsed_antismash_dir)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    region_df, count_df, presence_df = load_required_tables(
        parsed_antismash_dir=parsed_antismash_dir
    )

    isolate_summary = summarise_isolates(region_df=region_df, presence_df=presence_df)
    isolate_summary.to_csv(
        out_dir / "antismash_isolate_summary_extended.tsv",
        sep="\t",
        index=False,
    )

    product_summary = summarise_products(count_df=count_df, presence_df=presence_df)
    product_summary.to_csv(
        out_dir / "antismash_product_summary.tsv",
        sep="\t",
        index=False,
    )

    top_products_df = top_products_per_isolate(count_df=count_df, top_n=args.top_n)
    top_products_df.to_csv(
        out_dir / "antismash_top_products_per_isolate.tsv",
        sep="\t",
        index=False,
    )


if __name__ == "__main__":
    main()
