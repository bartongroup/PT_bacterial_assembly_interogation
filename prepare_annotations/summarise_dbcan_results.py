#!/usr/bin/env python3
"""
Summarise parsed dbCAN results into isolate- and family-level TSV outputs.

This script reads the parsed dbCAN outputs generated by
``parse_dbcan_results.py`` and creates compact summary tables for quick
inspection and downstream reporting.

Inputs
------
- dbcan_cazy_family_long.tsv
- dbcan_cazy_family_count_matrix.tsv
- dbcan_cazy_family_presence_absence_matrix.tsv

Outputs
-------
- dbcan_isolate_summary.tsv
- dbcan_family_summary.tsv
- dbcan_top_families_per_isolate.tsv
"""

from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd


def parse_args() -> argparse.Namespace:
    """Parse command-line arguments."""
    parser = argparse.ArgumentParser(
        description="Summarise parsed dbCAN CAZy annotation results."
    )
    parser.add_argument(
        "--parsed_dbcan_dir",
        required=True,
        help="Directory containing parsed dbCAN TSV outputs.",
    )
    parser.add_argument(
        "--out_dir",
        required=True,
        help="Output directory for dbCAN summary TSV files.",
    )
    parser.add_argument(
        "--top_n",
        type=int,
        default=10,
        help="Number of top CAZy families to report per isolate.",
    )
    return parser.parse_args()


def load_required_tables(parsed_dbcan_dir: Path) -> tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]:
    """
    Load required parsed dbCAN tables.

    Parameters
    ----------
    parsed_dbcan_dir : Path
        Directory containing parsed dbCAN TSV files.

    Returns
    -------
    tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]
        Long table, count matrix, and presence/absence matrix.
    """
    long_tsv = parsed_dbcan_dir / "dbcan_cazy_family_long.tsv"
    count_tsv = parsed_dbcan_dir / "dbcan_cazy_family_count_matrix.tsv"
    presence_tsv = parsed_dbcan_dir / "dbcan_cazy_family_presence_absence_matrix.tsv"

    for required_path in [long_tsv, count_tsv, presence_tsv]:
        if not required_path.exists():
            raise FileNotFoundError(f"Required dbCAN parsed file not found: {required_path}")

    long_df = pd.read_csv(long_tsv, sep="\t", dtype=str)
    count_df = pd.read_csv(count_tsv, sep="\t", dtype=str)
    presence_df = pd.read_csv(presence_tsv, sep="\t", dtype=str)

    return long_df, count_df, presence_df


def summarise_isolates(count_df: pd.DataFrame, presence_df: pd.DataFrame) -> pd.DataFrame:
    """
    Build isolate-level dbCAN summaries.

    Parameters
    ----------
    count_df : pd.DataFrame
        CAZy count matrix.
    presence_df : pd.DataFrame
        CAZy presence/absence matrix.

    Returns
    -------
    pd.DataFrame
        Isolate-level summary table.
    """
    count_num = count_df.copy()
    presence_num = presence_df.copy()

    family_columns = [column for column in count_num.columns if column != "isolate_id"]
    count_num[family_columns] = count_num[family_columns].apply(pd.to_numeric)
    presence_num[family_columns] = presence_num[family_columns].apply(pd.to_numeric)

    summary_df = pd.DataFrame(
        {
            "isolate_id": count_num["isolate_id"],
            "n_total_cazy_assignments": count_num[family_columns].sum(axis=1),
            "n_unique_cazy_families": presence_num[family_columns].sum(axis=1),
        }
    ).sort_values(
        by=["n_unique_cazy_families", "n_total_cazy_assignments", "isolate_id"],
        ascending=[False, False, True],
    )

    return summary_df


def summarise_families(count_df: pd.DataFrame, presence_df: pd.DataFrame) -> pd.DataFrame:
    """
    Build family-level dbCAN summaries.

    Parameters
    ----------
    count_df : pd.DataFrame
        CAZy count matrix.
    presence_df : pd.DataFrame
        CAZy presence/absence matrix.

    Returns
    -------
    pd.DataFrame
        Family-level summary table.
    """
    count_num = count_df.copy()
    presence_num = presence_df.copy()

    family_columns = [column for column in count_num.columns if column != "isolate_id"]
    count_num[family_columns] = count_num[family_columns].apply(pd.to_numeric)
    presence_num[family_columns] = presence_num[family_columns].apply(pd.to_numeric)

    records = []
    for family in family_columns:
        records.append(
            {
                "cazy_family": family,
                "n_isolates_present": int(presence_num[family].sum()),
                "n_total_assignments": int(count_num[family].sum()),
                "mean_assignments_per_isolate": float(count_num[family].mean()),
            }
        )

    return pd.DataFrame.from_records(records).sort_values(
        by=["n_isolates_present", "n_total_assignments", "cazy_family"],
        ascending=[False, False, True],
    )


def top_families_per_isolate(count_df: pd.DataFrame, top_n: int) -> pd.DataFrame:
    """
    Build a per-isolate table of the top CAZy families by count.

    Parameters
    ----------
    count_df : pd.DataFrame
        CAZy count matrix.
    top_n : int
        Number of top families to report per isolate.

    Returns
    -------
    pd.DataFrame
        Long-format per-isolate top-family table.
    """
    count_num = count_df.copy()
    family_columns = [column for column in count_num.columns if column != "isolate_id"]
    count_num[family_columns] = count_num[family_columns].apply(pd.to_numeric)

    records = []

    for _, row in count_num.iterrows():
        isolate_id = row["isolate_id"]
        isolate_series = row[family_columns]
        isolate_series = isolate_series[isolate_series > 0].sort_values(ascending=False).head(top_n)

        for rank, (family, value) in enumerate(isolate_series.items(), start=1):
            records.append(
                {
                    "isolate_id": isolate_id,
                    "rank": rank,
                    "cazy_family": family,
                    "assignment_count": int(value),
                }
            )

    if not records:
        return pd.DataFrame(
            columns=["isolate_id", "rank", "cazy_family", "assignment_count"]
        )

    return pd.DataFrame.from_records(records)


def main() -> None:
    """Run dbCAN summary generation."""
    args = parse_args()
    parsed_dbcan_dir = Path(args.parsed_dbcan_dir)
    out_dir = Path(args.out_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    long_df, count_df, presence_df = load_required_tables(parsed_dbcan_dir=parsed_dbcan_dir)

    isolate_summary = summarise_isolates(count_df=count_df, presence_df=presence_df)
    isolate_summary.to_csv(out_dir / "dbcan_isolate_summary.tsv", sep="\t", index=False)

    family_summary = summarise_families(count_df=count_df, presence_df=presence_df)
    family_summary.to_csv(out_dir / "dbcan_family_summary.tsv", sep="\t", index=False)

    top_family_df = top_families_per_isolate(count_df=count_df, top_n=args.top_n)
    top_family_df.to_csv(out_dir / "dbcan_top_families_per_isolate.tsv", sep="\t", index=False)


if __name__ == "__main__":
    main()
